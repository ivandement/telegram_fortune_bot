def get_card_image_path(card_name: str):
    return os.path.join("cards", f"{card_name}.jpg")


def calculate_life_path(birthdate: str) -> int:
    digits = [int(ch) for ch in birthdate if ch.isdigit()]
    total = sum(digits)
    while total > 9 and total not in (11, 22):
        total = sum(int(ch) for ch in str(total))
    if total in (11, 22):
        total = sum(int(ch) for ch in str(total))
    return total if 1 <= total <= 9 else 5


def detect_theme(question: str) -> str:
    q = question.lower()

    love_words = ["люб", "отнош", "парен", "девуш", "муж", "жен", "сердц", "расстав", "ревн"]
    work_words = ["работ", "деньг", "карьер", "проект", "бизнес", "коллег", "професс"]
    anxiety_words = ["трев", "страх", "бою", "не уверен", "неувер", "устал", "тяжело"]
    future_words = ["будущ", "что будет", "дальше", "ждет", "произойдет"]

    if any(w in q for w in love_words):
        return "love"
    if any(w in q for w in work_words):
        return "work"
    if any(w in q for w in anxiety_words):
        return "anxiety"
    if any(w in q for w in future_words):
        return "future"
    return "general"


