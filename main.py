    part1 = build_card_interpretation(cards[0], "past")
    part2 = build_card_interpretation(cards[1], "present")
    part3 = build_card_interpretation(cards[2], "future")

    summary = build_psychological_summary(question, cards, birthdate)

    return (
        f"{intro}\n\n"
        f"💭 Твой вопрос: {question}\n\n"
        f"{part1}\n\n"
        f"{part2}\n\n"
        f"{part3}\n\n"
        f"💫 Глубинный психологический смысл расклада:\n"
        f"{summary}"
    )


